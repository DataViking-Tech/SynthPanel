"""Markdown renderer for post-hoc panel reporting.

Pure renderer: consumes an :class:`InspectReport` plus the raw panel JSON
dict and returns a complete Markdown document. No I/O, no mutation of the
input. The caller is responsible for writing the returned string to stdout
or disk.

Section order and the mandatory synthetic-panel banner/footer strings are
fixed by spec (see ``specs/sp-viz-layer/structure.md`` §2 and §7) and are
enforced by tests.
"""

from __future__ import annotations

from typing import TYPE_CHECKING, Any

if TYPE_CHECKING:
    from synth_panel.analysis.inspect import InspectReport


BANNER = (
    "> **Synthetic panel.** All responses below were generated by AI personas,\n"
    "> not human respondents. Do not cite as user-research data."
)

FOOTER = "_Generated by synthpanel report. Synthetic panel — AI-generated responses, not human data._"


def render_markdown(
    report: InspectReport,
    raw: dict[str, Any],
    *,
    source_path: str | None = None,
) -> str:
    """Render a complete Markdown report for a saved panel result.

    Pure function. Sections are emitted in the order defined by
    structure.md §2; the banner and footer are hard-coded and verbatim.
    """

    parts: list[str] = []

    title_id = report.result_id or "(unsaved result)"
    parts.append(f"# Panel Report: {title_id}")
    parts.append("")
    parts.append(BANNER)
    parts.append("")

    parts.extend(_provenance_block(report, raw, source_path=source_path))
    parts.append("")

    parts.extend(_overview_block(report))
    parts.append("")

    parts.extend(_model_rollup_block(report))
    parts.append("")

    parts.extend(_persona_summary_block(report))
    parts.append("")

    parts.extend(_synthesis_block(report, raw))
    parts.append("")

    parts.extend(_failure_stats_block(report))

    if report.warnings:
        parts.append("")
        parts.extend(_warnings_block(report))

    parts.append("")
    parts.append(FOOTER)

    return "\n".join(parts) + "\n"


