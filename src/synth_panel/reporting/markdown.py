def _provenance_block(
    report: InspectReport,
    raw: dict[str, Any],
    *,
    source_path: str | None,
) -> list[str]:
    """Section 3 — provenance table per structure.md §7.

    Missing fields degrade to ``(not recorded)`` / ``(unknown)`` rather
    than raising ``KeyError``. Rows for optional fields (``total_seconds``,
    ``synthesis_model``) are omitted when the source data is absent.
    """
    metadata = raw.get("metadata") if isinstance(raw.get("metadata"), dict) else {}
    version = metadata.get("version") if isinstance(metadata.get("version"), dict) else {}
    cost_meta = metadata.get("cost") if isinstance(metadata.get("cost"), dict) else {}
    timing = metadata.get("timing") if isinstance(metadata.get("timing"), dict) else {}
    models_meta = metadata.get("models") if isinstance(metadata.get("models"), dict) else {}

    config_hash = metadata.get("config_hash")
    synthpanel_version = version.get("synthpanel")
    python_version = version.get("python")
    pricing_snapshot_date = cost_meta.get("pricing_snapshot_date")
    created_at = raw.get("created_at") or report.created_at
    total_seconds = timing.get("total_seconds")

    panelist_models_meta = models_meta.get("panelist")
    if isinstance(panelist_models_meta, list) and panelist_models_meta:
        panelist_models_str = ", ".join(str(m) for m in panelist_models_meta)
    elif report.models:
        panelist_models_str = ", ".join(report.models)
    elif report.model:
        panelist_models_str = report.model
    else:
        panelist_models_str = "(unknown)"

    synthesis_model = models_meta.get("synthesis") or report.synthesis.model

    rows: list[tuple[str, str]] = [
        ("config_hash", _fmt(config_hash, missing="(not recorded)")),
        ("synthpanel_version", _fmt(synthpanel_version)),
        ("python_version", _fmt(python_version)),
        ("pricing_snapshot_date", _fmt(pricing_snapshot_date)),
        ("created_at", _fmt(created_at)),
    ]
    if total_seconds is not None:
        rows.append(("total_seconds", f"{total_seconds}"))
    rows.append(
        (
            "source_path",
            source_path if source_path else "(loaded by ID)",
        )
    )
    rows.append(("panelist_model(s)", panelist_models_str))
    if synthesis_model:
        rows.append(("synthesis_model", str(synthesis_model)))

    lines: list[str] = ["## Provenance", ""]
    lines.extend(_md_table(["Field", "Value"], rows))
    return lines


def _overview_block(report: InspectReport) -> list[str]:
    """Section 4 — overview counts + path + cost header.

    The cost row uses ``report.total_cost`` as the pre-formatted
    ``$0.0000`` string produced upstream by ``format_summary()`` (design
    §Rationale, seam 2) — no float re-formatting.
    """
    lines: list[str] = ["## Overview", ""]
    rows: list[tuple[str, str]] = [
        ("Personas", str(report.persona_count)),
        ("Questions", str(report.question_count)),
        ("Rounds", _rounds_cell(report)),
        ("Total tokens", str(report.total_tokens)),
    ]
    if report.total_cost:
        rows.append(("Total cost", report.total_cost))
    if report.panelist_cost:
        rows.append(("Panelist cost", report.panelist_cost))
    if report.instrument_name:
        rows.append(("Instrument", report.instrument_name))
    if report.extraction_present:
        rows.append(("Extraction", "present (post-hoc structured extraction used)"))
    if not report.has_rounds_shape:
        rows.append(("Shape", "flat (saved via --save; no rounds metadata)"))

    lines.extend(_md_table(["Field", "Value"], rows))

    if report.path:
        lines.append("")
        lines.append(f"**Path:** {' → '.join(_escape_md(p) for p in report.path)}")
    return lines


def _model_rollup_block(report: InspectReport) -> list[str]:
    """Section 5 — per-model rollup table."""
    lines: list[str] = ["## Per-Model Rollup", ""]
    if not report.model_rollup:
        lines.append("_No model usage recorded._")
        return lines
    headers = ["Model", "Personas", "Answered", "Input", "Output", "Total", "Cost"]
    rows: list[tuple[str, ...]] = []
    for m in report.model_rollup:
        cost = f"${m.cost_usd:.4f}" if m.cost_usd is not None else "—"
        rows.append(
            (
                m.model,
                str(m.personas),
                str(m.questions_answered),
                str(m.input_tokens),
                str(m.output_tokens),
                str(m.total_tokens),
                cost,
            )
        )
    lines.extend(_md_table(headers, rows))
    return lines


def _persona_summary_block(report: InspectReport) -> list[str]:
    """Section 6 — per-persona summary table."""
    lines: list[str] = ["## Per-Persona Summary", ""]
    if not report.personas:
        lines.append("_No panelists recorded._")
        return lines
    headers = ["Persona", "Model", "Responses", "Errors", "Notes"]
    rows: list[tuple[str, ...]] = []
    for p in report.personas:
        rows.append(
            (
                p.name,
                p.model or "(unknown)",
                str(p.response_count),
                str(p.error_count),
                p.panelist_error or "",
            )
        )
    lines.extend(_md_table(headers, rows))
    return lines


def _synthesis_block(report: InspectReport, raw: dict[str, Any]) -> list[str]:
    """Section 7 — synthesis status, meta, summary peek, plus the full
    themes / agreements / disagreements / recommendation lists pulled
    straight from ``raw['synthesis']`` so the report is no longer just a
    240-char peek over a black-box JSON blob (sp-xltd).
    """
    lines: list[str] = ["## Synthesis", ""]
    s = report.synthesis
    if not s.ran and s.error is None and s.summary_peek is None:
        lines.append("_Not run._")
        return lines
    if s.error and not s.ran:
        lines.append(f"**Status:** failed — {_escape_inline(s.error)}")
        return lines
    lines.append("**Status:** ran")
    meta_rows: list[tuple[str, str]] = []
    if s.model:
        meta_rows.append(("Model", str(s.model)))
    if s.cost:
        meta_rows.append(("Cost", str(s.cost)))
    if s.theme_count:
        meta_rows.append(("Themes", str(s.theme_count)))
    if meta_rows:
        lines.append("")
        lines.extend(_md_table(["Field", "Value"], meta_rows))
    if s.summary_peek:
        lines.append("")
        lines.append("**Summary peek:**")
        lines.append("")
        lines.append("> " + s.summary_peek.replace("\n", "\n> "))

    synth = raw.get("synthesis") if isinstance(raw.get("synthesis"), dict) else {}

    themes = synth.get("themes")
    if isinstance(themes, list) and themes:
        lines.append("")
        lines.append("**Themes:**")
        lines.append("")
        for item in themes:
            lines.append(_render_theme_item(item))

    for label, key in (("Agreements", "agreements"), ("Disagreements", "disagreements")):
        items = synth.get(key)
        if isinstance(items, list) and items:
            lines.append("")
            lines.append(f"**{label}:**")
            lines.append("")
            for i, entry in enumerate(items, 1):
                lines.append(f"{i}. {_format_synth_item(entry)}")

    recommendation = synth.get("recommendation")
    rec_text = _coerce_str(recommendation).strip() if recommendation else ""
    if rec_text:
        lines.append("")
        lines.append("**Recommendation:**")
        lines.append("")
        text = _truncate(rec_text, _SYNTH_ITEM_CAP)
        lines.append("> " + _escape_inline(text).replace("\n", "\n> "))

    return lines


def _failure_stats_block(report: InspectReport) -> list[str]:
    """Section 8 — failure stats."""
    fs = report.failure_stats
    lines: list[str] = ["## Failure Stats", ""]
    rows: list[tuple[str, str]] = [
        ("Total pairs", str(fs.total_pairs)),
        ("Errored pairs", str(fs.errored_pairs)),
        ("Failure rate", f"{fs.failure_rate:.1%}"),
    ]
    if fs.failed_panelists:
        rows.append(("Failed panelists", str(fs.failed_panelists)))
    if fs.errored_personas:
        shown = ", ".join(_escape_md(n) for n in fs.errored_personas[:6])
        extra = len(fs.errored_personas) - 6
        if extra > 0:
            shown += f", +{extra} more"
        rows.append(("Affected personas", shown))
    lines.extend(_md_table(["Field", "Value"], rows))
    return lines


def _warnings_block(report: InspectReport) -> list[str]:
    """Section 9 — warnings (only rendered when present)."""
    lines: list[str] = ["## Warnings", ""]
    for w in report.warnings:
        lines.append(f"- {_escape_inline(w)}")
    return lines


